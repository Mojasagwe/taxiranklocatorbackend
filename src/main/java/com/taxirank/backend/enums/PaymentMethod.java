package com.taxirank.backend.enums;

/**
 * Enum representing the different payment methods available for users.
 */
public enum PaymentMethod {
    /**
     * Cash payment method
     */
    CASH,
    
    /**
     * Card payment method (credit/debit)
     */
    CARD,
    
    /**
     * Mobile money payment method
     */
    MOBILE_MONEY,
    
    /**
     * Digital wallet payment method
     */
    WALLET
} 